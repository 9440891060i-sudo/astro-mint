import { useState } from "react";
import { useNavigate, useLocation } from "react-router-dom";
import { Home, Rocket, Plus, Play, DollarSign } from "lucide-react";

type TabType = "home" | "launch" | "trade" | "reels" | "assets";

const BottomNav = () => {
  const navigate = useNavigate();
  const location = useLocation();

  // Check if we're on a company profile that was navigated from home or reels
  const fromPath = location.state?.from;
  const isCompanyProfile = location.pathname.startsWith('/company/');
  
  const tabs = [
    { id: "home" as TabType, icon: Home, label: "Home", path: "/" },
    { id: "launch" as TabType, icon: Rocket, label: "Launch", path: "/launch" },
    { id: "trade" as TabType, icon: Plus, label: "Trade", path: "/trade" },
    { id: "reels" as TabType, icon: Play, label: "Reels", path: "/reels" },
    { id: "assets" as TabType, icon: DollarSign, label: "Assets", path: "/assets" },
  ];

  const handleTabClick = (tab: typeof tabs[0]) => {
    navigate(tab.path);
  };

  return (
    <nav className="fixed bottom-0 left-0 right-0 bg-background border-t border-border z-50">
      <div className="max-w-2xl mx-auto flex items-center justify-around h-16">
        {tabs.map((tab) => {
          const Icon = tab.icon;
          // Keep the original tab active if we're on company profile from that tab
          const isActive = isCompanyProfile && fromPath
            ? tab.path === fromPath
            : location.pathname === tab.path;
          const isMainAction = tab.id === "trade";

          return (
            <button
              key={tab.id}
              onClick={() => handleTabClick(tab)}
              className="flex flex-col items-center justify-center gap-1 flex-1 hover:opacity-70"
              aria-label={tab.label}
            >
              {isMainAction ? (
                // Trade button - larger with square background
                <div className="w-10 h-10 rounded-lg border border-foreground flex items-center justify-center">
                  <Icon
                    className="text-foreground"
                    strokeWidth={isActive ? 2 : 1.5}
                    size={24}
                  />
                </div>
              ) : (
                // Regular nav icons
                <Icon
                  className="text-foreground"
                  strokeWidth={isActive ? 2 : 1.5}
                  size={24}
                  fill={isActive ? "currentColor" : "none"}
                />
              )}
            </button>
          );
        })}
      </div>
    </nav>
  );
};

export default BottomNav;
